class dtype:
    """
    Definitions of all data types currently supported.
    """

    # Numerical type data
    integer = "integer"
    float = "float"
    quantity = "quantity"

    # Categorical type data
    binary = "binary"
    categorical = "categorical"
    tags = "tags"

    # Dates and Times (time-series)
    date = "date"
    datetime = "datetime"

    # Text
    short_text = "short_text"
    rich_text = "rich_text"

    # Complex Data types
    image = "image"
    audio = "audio"
    video = "video"

    # Series/Sequences
    array = "array"

    # Misc (Unk/NaNs)
    empty = "empty"
    invalid = "invalid"

